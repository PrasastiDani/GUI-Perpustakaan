    private void btnSimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSimpanMouseClicked
        // TODO add your handling code here:
        
        try {
            this.printDataBuku();
        } catch (TahunTerbitHarusAngka e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (TahunTerbitHarus4Digit e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        try{
            Connection conn = KoneksiDB.getKoneksi();
            String query = "INSERT INTO tb_buku" + "(judul,penulis,tahun)" + "VALUE(?,?,?)";
            PreparedStatement p = conn.prepareStatement(query);
            
            p.setString(1, txtJudul.getText());
            p.setString(2, txtPenulis.getText());
            p.setString(3, txtTahun.getText());
            
            int rowsInserted = p.executeUpdate();
            if(rowsInserted > 0){
                JOptionPane.showMessageDialog(this, "Data Buku Berhasil Disimpan", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
            p.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data Buku Gagal Disimpan", "Error", JOptionPane.ERROR_MESSAGE);
        }
        finally{
            this.loadDataToTable();
        }
    }//GEN-LAST:event_btnSimpanMouseClicked

    private void btnUbahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUbahMouseClicked
        // TODO add your handling code here:
        try {
            Connection conn = KoneksiDB.getKoneksi();
            String query = "UPDATE tb_buku SET judul=?,penulis=?,tahun=? WHERE id_buku=?";
            PreparedStatement p = conn.prepareStatement(query);
            
            p.setString(1, txtJudul.getText());
            p.setString(2, txtPenulis.getText());
            p.setString(3, txtTahun.getText());
            p.setString(4, txtID.getText());
            
            int rowsUpdate = p.executeUpdate();
            if(rowsUpdate > 0){
                JOptionPane.showMessageDialog(this, "Data Buku Berhasil diUpdate", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
            p.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data Buku Gagal diUpdate", "Error", JOptionPane.ERROR_MESSAGE);
        }
        finally{
            this.loadDataToTable();
        }
    }//GEN-LAST:event_btnUbahMouseClicked

    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        // TODO add your handling code here:
        if(!tabel_klik.equals("")){
            try {
                Connection conn = KoneksiDB.getKoneksi();
                String query = "DELETE FROM tb_buku WHERE id_buku=?";
                PreparedStatement p = conn.prepareStatement(query);
                p.setString(1, txtID.getText());
                int rowsDeleted = p.executeUpdate();
                if(rowsDeleted > 0){
                    JOptionPane.showMessageDialog(this, "Data Buku Berhasil diHapus", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
                p.close();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Data Buku Berhasil diHapus", "Error", JOptionPane.ERROR_MESSAGE);
            }
            finally{
                this.loadDataToTable();
            }
        }
    }//GEN-LAST:event_btnDeleteMouseClicked

