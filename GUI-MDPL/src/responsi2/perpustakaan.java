    public void loadDataToTable(){
        DefaultTableModel dm = (DefaultTableModel)tbDatabase.getModel();
        while (dm.getRowCount() > 0){
            dm.removeRow(0);
        }
        
        try {
            Connection conn=KoneksiDB.getKoneksi();
            Statement s = conn.createStatement();
            String query ="SELECT * FROM tb_buku";
            ResultSet result = s.executeQuery(query);
            
            while(result.next()){
                Object [] obj = new Object [4];
                obj[0] = result.getString("id_buku");
                obj[1] = result.getString("judul");
                obj[2] = result.getString("penulis");
                obj[3] = result.getInt("tahun");
                
                model.addRow(obj);
            }
            result.close();
            s.close();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        finally{
            tabel_klik = "";
        }
    }
 
    public void printDataBuku() throws TahunTerbitHarusAngka, TahunTerbitHarus4Digit{
        int tahun;
        
        try {
            tahun = Integer.parseInt(txtTahun.getText());
        } catch (NumberFormatException e) {
            throw new TahunTerbitHarusAngka();
        }
        
        tahun = txtTahun.getText().length();
        if (tahun < 4 || tahun > 4){
            throw new TahunTerbitHarus4Digit();
        }
        
        }
